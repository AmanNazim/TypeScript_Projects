let personName: string = "aMan";//aMan

let lowerCase: string = personName.toLowerCase();//lowerCase
let upperCase: string = personName.toUpperCase();//upperCase
let titleCase: string = personName.charAt(0).toUpperCase() + personName.slice(1).toLowerCase() ;//titleCase

//Person name with lowerCase.
console.log(lowerCase);//aman
//Person name with upperCase.
console.log(upperCase);//AMAN
//person name with titleCase.
console.log(titleCase);//Aman