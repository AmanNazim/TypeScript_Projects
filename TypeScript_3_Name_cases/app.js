let personName = "aMan"; //aMan
let lowerCase = personName.toLowerCase(); //lowerCase
let upperCase = personName.toUpperCase(); //upperCase
let titleCase = personName.charAt(0).toUpperCase() + personName.slice(1).toLowerCase(); //titleCase
//Person name with lowerCase.
console.log(lowerCase); //aman
//Person name with upperCase.
console.log(upperCase); //AMAN
//person name with titleCase.
console.log(titleCase); //Aman
